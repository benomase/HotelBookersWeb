package com.models;

public class PhotosModel {
	private Long id;
	private Long nom;
	private String url;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getNom() {
		return nom;
	}
	public void setNom(Long nom) {
		this.nom = nom;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	
	
}
